    def draw(self, display_surf, image_surf, img):
        bx = 0
        by = 0
        for i in range(0, self.M * self.N):
            if self.maze[bx + (by * self.M)] == 1:
                display_surf.blit(image_surf, (bx * 29, by * 30))
            
            if self.maze[bx + (by * self.M)] == 2:
                display_surf.blit(img, (bx * 29, by * 30))

            bx = bx + 1
            if bx > self.M - 1:
                bx = 0
                by = by + 1

    def get_total(self):
        sum = 0
        for i in range(0, self.M * self.N):
            if self.maze[i] == 0 or self.maze[i] == 2:
                sum = sum + 1
        return sum * 100

    def value(self, x, y):
        ret = 0
        if self.maze[x + (y * self.M)] == 1:
            ret = 1
        if self.maze[x + (y * self.M)] == 2:
            ret = 1
        return ret
