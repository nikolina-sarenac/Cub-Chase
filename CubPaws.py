        by = 0
        for i in range(0, self.M * self.N):
            if self.matr[bx + (by * self.M)] == 1:
                display.blit(image, (bx * 29, by * 30))

            bx = bx + 1
            if bx > self.M - 1:
                bx = 0
                by = by + 1

